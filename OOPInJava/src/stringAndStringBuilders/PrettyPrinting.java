package stringAndStringBuilders;

public class PrettyPrinting {

	public static void main(String[] args) {
		float a = 23.23546f;
		System.out.printf("forated number is %.2f",a);
		System.out.println();
		System.out.printf( "pie %.2f",Math.PI);

		System.out.println();
		System.out.printf("Hello! my name is %s i am %s","Prasad","cool");
		
		
		//there are so many place holders in java as given below
		/*
		 * %c - Character
%d - Decimal number (base 10)
%e - Exponential floating-point number
%f - Floating-point number
%i - Integer (base 10)
%o - Octal number (base 8)
%s - String
%u - Unsigned decimal (integer) number
%x - Hexadecimal number (base 16)
%t - Date/time
%n - Newline

		 */
	}

}
